/**
 * Carga y valida el dataset histórico.
 * Retorna la matriz de datos lista para el bootstrap.
 */
export function loadHistoricalData(): number[][] {
  return HISTORICAL_DATA.map(row => {
    const [, rvg, rfg, sura, afp, rfcl, ipc, hicp, dfx] = row;
    // Para filas con SURA o AFP null, usar proxy basado en blend
    // El motor de simulación maneja esto internamente
    return [
      rvg,
      rfg,
      sura ?? (0.70 * rvg + 0.15 * rfcl + 0.15 * rfg),  // proxy SURA
      afp  ?? (0.55 * rvg + 0.20 * rfcl + 0.25 * rfcl),  // proxy AFP
      rfcl,
      ipc,
      hicp,
      dfx,
    ];
  });
}

/**
 * Estadísticas del dataset para diagnóstico.
 */
export function getDatasetStats() {
  const n = HISTORICAL_DATA.length;
  const nSura = HISTORICAL_DATA.filter(r => r[3] !== null).length;
  const nAfp  = HISTORICAL_DATA.filter(r => r[4] !== null).length;
  return {
    totalMonths: n,
    periodStart: HISTORICAL_DATA[0][0],
    periodEnd:   HISTORICAL_DATA[n-1][0],
    suraMonths:  nSura,
    afpMonths:   nAfp,
    dataQuality: nAfp / n,
  };
}
